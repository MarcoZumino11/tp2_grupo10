package ar.edu.unju.escmi.tp2.ejercicio1;

public class ejercicio1 {
    public static void main(String[] args) {
            // Número de documento
        int dni = 45789321;
        System.out.println("Mi DNI es: " + dni);

        // Letra ejemplar del DNI
        char ejemplar = 'A';
        System.out.println("La letra ejemplar de mi DNI es: " + ejemplar);

        // Edad de un familiar
        int edadMama = 50;
        System.out.println("La edad de mi mamá es: " + edadMama + " años");

        // Nombre de un prócer argentino
        String procer = "José de San Martín";
        System.out.println("Nombre de un prócer argentino: " + procer);

        // Número de bingo
        int numeroBingo = 42;
        System.out.println("El número de bingo es: " + numeroBingo);

        // Precio de un celular
        double precioCelular = 299999.99;
        System.out.println("El precio del celular es: $" + precioCelular);

        // Estatura en metros
        double estatura = 1.75;
        System.out.println("Mi estatura es: " + estatura + " metros");

        // Peso de mi mascota
        double pesoMascota = 12.5;
        System.out.println("El peso aproximado de mi mascota es: " + pesoMascota + " kg");

        // Fecha de inicio del segundo cuatrimestre
        String inicioCuatrimestre = "05/08/2025";
        System.out.println("El segundo cuatrimestre inicia el: " + inicioCuatrimestre);

        // Fecha de finalización del segundo cuatrimestre
        String finCuatrimestre = "20/12/2025";
        System.out.println("El segundo cuatrimestre finaliza el: " + finCuatrimestre);
    }

}
